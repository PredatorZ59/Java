import java.util.Scanner;

public class PG5 {
    public static void main(String[] args) {
		 
		Scanner ti=new Scanner(System.in);
		System.out.println("Enter the Value of A(Integer Value):");
		int a=ti.nextInt();
		System.out.println("Enter the Value of B(Float Value):");
		float b=ti.nextFloat();
		System.out.println("Enter the Value of C(Sting Value):");
		String c=ti.next();
		System.out.println("Enter the Value of D(Character Value:");
		char d=ti.next().charAt(0);
		
	System.out.println("A="+a);	
	System.out.println("B="+b);	
	System.out.println("C="+c);
	System.out.println("D="+d);

	}

    
}
